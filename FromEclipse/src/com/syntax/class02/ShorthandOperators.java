package com.syntax.class02;

public class ShorthandOperators {

	public static void main(String[] args) {
		
		
		int num=20;
		num=num+100;// 20+100=120
		System.out.println(num);
		
		num=num-60;//120-60=60
		System.out.println(num);
		
		int number=100;
		
		number+=100;//number=number+100
		
		System.out.println(number);
		
		number-=30;// number=number-30
		
		System.out.println(number);//200(number)- 30=170
		
		
		number/=10;
		System.out.println(number);//now the new variable is 170(number) 
		/*
		 * and we're asking java to divide the by ten which equals 17
		 */
		
		number*=2;
		System.out.println(number);
		
		/*
		 * now that the new variable(number) is 17 we're asking java to multiply by 2 which equals 34
		 */
		
		
	
		
		
		
		

	}

}
