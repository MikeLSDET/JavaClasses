package com.syntax.class10;

public class OdometerExcersiseFreeTime {

	public static void main(String[] args) {
		// create an odometer up to 99999
		
		
		for(int i=0; i<10; i++) {
			
			
			
			for (int j=0; j<10; j++) {
				
				for(int k=0; k<10; k++) {
					
					for(int l=0; l<10; l++) {
						
						for(int z=0; z<10; z++) {
							System.out.println(i+" "+j+" "+k+" "+l+" "+z);
									
						}
					}
				}
				
			}
		}

	}

}
