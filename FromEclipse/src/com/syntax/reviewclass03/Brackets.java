package com.syntax.reviewclass03;

public class Brackets {

	public static void main(String[] args) {
		
		
		int number=10;
		
		if(number==10) {
			System.out.println("If with brackets");
			System.out.println("If with brackets");
			System.out.println("If with brackets");
		}

		if(number==10) 
			System.out.println("If without brackets");
			System.out.println("If without brackets");
			System.out.println("If without brackets");
		
		
		
		
	}

}
